package com.metabroadcast.atlas.glycerin;

public class GlycerinException extends Exception {

    public GlycerinException() {
        super();
    }

    public GlycerinException(String message) {
        super(message);
    }

    public GlycerinException(Throwable cause) {
        super(cause);
    }

    public GlycerinException(String message, Throwable cause) {
        super(message, cause);
    }

}